# python lists
# python lists are mutable
d = [5, 10, 15, 20, 25, 30, 35, 40]

print("d[2]= ", d[2])
print("d[0:3]= ", d[0:3])
print("d[5:]= ", d[5:])

# python tuples
# tuples are immutable
e = (25, 'program', 1+3j)

print("e[1]= ", e[1])
print("e[0:2]= ", e[0:2])
print("e[0:]= ", e[0:])

s = 'Hello World!'
print("s[4]= ", s[4])

